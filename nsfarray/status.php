            The samples are from rice leaves treated with wild type Xanthomonas oryzae pv. oryzae culture 
            (Xoo) vs. a mutant Xoo control.  2ug of mRNA was used for indirect labeling. The probe was 
            hybridized at 55C in Geneframes buffer from MWG.  Slides were scanned using an axon 4000 
            scanner and spot finding was done using Genepix. 063714 and 063715 are dye swaps of the same 
            RNA sample. 

	    <br/>
            <br/>
            --
            <br/>
            <br/>

	    Our second version of the array "Rice oligo version 2" contains 20,230
	    oligonucleotides which were designed using gene models from 
            <a href="http://www.tigr.org/tdb/e2k1/osa1/">the TIGR Rice Annotation Database</a> 
            that have EST and/or full-length cDNA support. These oligos were cross referenced to 
            <a href="http://cdna01.dna.affrc.go.jp/cDNA/">the Kikuchi full-length cDNA dataset</a> 
            and were designed using 
            <a href="http://bioinformatics.oupjournals.org/cgi/content/abstract/bth347v1">PICKY</a>.
            They are now being printed at UCD and slides can now be ordered through this web site (<a href="order.shtml">order form</a>).
            The cost is $110/slide. 

            <br/>
            <br/>	   

	    If requested, <a href="http://array.ucdavis.edu/">ArrayCore</a> is able to provide a hybridization service. 
	    Users should send their labelled probes vacuum centrifuged and ready to hybridize 
            (ArrayCore will supply <a href="http://array.ucdavis.edu/protocols.jsp">protocols</a> 
            for recommended procedures). The current rate for off-campus users who would like to use 
            the hybridization service is $678.26 for 4-12 slides (4 slides minimum).  
	    This includes the cost of the hybridization, scanning, and spot-finding analysis.  
	    This works out to be $56.52 per slide (assuming 12 slides).
	    The raw data will then be sent to the user for analysis.  
	    <a href="http://www.iobion.com/">Iobion</a> 
            <a href="http://www.iobion.com/products/products_GENETRAFFIC.html#duo">GeneTraffic Duo</a> 
            is available for data analysis by ArrayCore users via our webserver and the cost associated 
            with this service for off-campus users is $24.16 per slide.

            <br/>
            <br/>	    

	    TIGR is also in the process of developing a Rice Oligonucleotide Microarray Database which will allow users to store 
	    expression data generated from the arrays as a free service.  
	    Expression data submitted will be analyzed and available for comparative studies with all publicly available rice array data.
	    The expression database aims for complete compliance with the <a href="http://www.mged.org/Workgroups/MIAME/miame_checklist.html">MIAME</a> and <a href="http://www.plantontology.org">Plant Ontology</a> standards that have been developed.


