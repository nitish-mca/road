                    <h3>Why Rice Oligonucleotide Arrays?</h3>
                    <br/>

                    Microarray technology permits biologists to concurrently measure the
                    expression levels of thousands of genes in a single experiment.
                    Public-access to microarrays is critical to ensure that the public's large
                    investment in rice genomics is converted to public scientific and economic
                    benefits. 

                    <br/>
                    <br/>

                    Because of its small genome size, extensive genetic map, available genome
                    sequence, and relative ease of transformation, rice is considered a model
                    species for monocots. Gene sequences are relatively conserved among
                    economically agricultural cereal species such as rice, maize, wheat, barley,
                    sorghum, rye, and sugarcane, and therefore, structural and functional
                    analysis of rice has broad practical implications. Draft rice genomic
                    sequence has been generated by <a href="http://www.rice-research.org/">Monsanto</a> (<i>japonica</i> cultivar Nipponbare),
                    <a href="http://www.sciencemag.org/cgi/content/full/296/5565/92">Syngenta</a> (<i>japonica</i> cultivar Nipponbare) and the <a href="http://www.sciencemag.org/cgi/content/full/296/5565/79">Beijing Genomics Institute</a>
                    (<i>indica</i> cultivar). Moreover, <a href="http://www.ncbi.nlm.nih.gov/dbEST/dbEST_summary.html">>260,000 ESTs</a> and <a href="http://www.sciencemag.org/cgi/content/full/301/5631/376">>28,000 full length cDNA</a>
                    sequences are also available.

                    <br/>
                    <br/>

                    <a href="http://rgp.dna.affrc.go.jp/IRGSP/">The International Rice Genome Sequencing Project</a> (IRGSP) has now produced
                    a finished rice (<i>O. sativa</i> spp. <i>japonica</i> cultivar Nipponbare) <a href="http://rgp.dna.affrc.go.jp/IRGSP/download.html">genome
                        sequence</a>.

                    <br/>
