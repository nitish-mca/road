                <h3>Rice Oligonucleotide Microarray (ROMA) Expression Database</h3>
                <br/>

                The ROMA database at TIGR is designed to support expression data from all publicly available rice array 
                platforms including the NSF Rice Oligonucleotide Array Project, Agilent arrays, Affymetrix arrays, and the 
                Yale/BGI oligonucleotide array. Please note that these publicly available data are available at other repositories 
                such as the <a href='http://www.ncbi.nlm.nih.gov/geo/'>NCBI Gene Expression Omnibus</a> and in the case of the Yale/BGI array, 
                at the <a href='http://plantgenomics.biology.yale.edu/riceatlas/'>Virtual Center for Cellular Expression Profiling of Rice</a>. 
                Please note that we process the expression data using our methods (see URL) and thus our normalized data 
                may differ from that at other expression databases

