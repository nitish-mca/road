                    <h3>Objectives of the NSF Rice Oligonucleotide Array Project</h3>
                    <br/>

            <li>Design 50-70-mer oligomers based on finished rice genomic sequence.
                </br>
            <li>Distribute 2 generations of rice microarrays. The second version will
                correspond to the "whole genome".
                </br>
            <li>Construct a data analysis system, database and user interface for microarray 
                expression data. Develop web-based tools for data mining the rice transcriptome.
                </br>
            <li>Develop training workshops in plant microarray experimentation.

