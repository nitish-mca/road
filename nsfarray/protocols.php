                    <h2>Experimental Protocols</h2>
                    <br/>

                    The NSF Rice Oligo Array project has fabricated two arrays for public use: the 20K array released 
                    in late 2003 and the 45K array to be released in February 2006. 
                    Please note that the two slide types utilize different slide chemistries and thus, 
                    the protocols to be used with each slide type differ.

                    <br/>
                    <br/>

                    The 20K array was printed at the <a href='http://array.ucdavis.edu/'>UCD Array Core Facility</a> on primarily epoxy slides with 
                    a small number (after September 2005) printed on amine slides. Protocols for use with the 20K arrays are listed below:

                    <br/>
                    <br/>

                    Array protocols available for download (updated Jan 9, 2006): 

                    <br/>
                    <br/>

                    <ul>
                        <li><a href='protocols/ArrayCore_RiceArray_18C03A.zip'>NSF Rice Oligonucleotide Arrays - Recommendations</a> (Word, zip, 6k)</li>
                        <li><a href='protocols/ArrayCore_RiceArray_NaBorohydride.zip'>Sodium Borohydride Pre-treatment Protocol for Amino Slides</a> (Word, zip, 5k)</li>
                        <li><a href='protocols/ArrayCore_RiceArray_Blocking.zip'>Blocking Protocol for Amino Slides</a> (Word, zip, 4k)</li>
                        <li><a href='protocols/mRNA_Isolation_using_Oligotex.zip'>mRNA Isolation using Oligotex</a> (Word, zip, 5k)</li>
                        <li><a href='protocols/Total_RNA_isolation.zip'>Total RNA Isolation</a> (Word, zip, 4k)</li>

                    </ul>

                    <br/>
                    <br/>

                    Additional array protocols may be accessed at <a href='http://array.ucdavis.edu/protocols.jsp'>UCD ArrayCore Protocols</a>.

                    <br/>
                    <br/>

                    The 45K array was printed at TIGR using Corning UltraGAPS slides. The protocol for use with the TIGR 45K arrays is available below:

                    <ul>
                        <li><a href='protocols/Rice_aRNA_method_TIGR_Slides_Jan09_2006.zip'>mRNA Amplification and Hybridizations</a> (Word, zip, 90k)</li>
                    </ul>

